    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        double eur=1.17, dol=0.85, unit, result;
        String eurString, dolString;
        
        try{
            if(!equivalecia.getText().equals("")){
                String data = equivalecia.getText();
                dol = Double.parseDouble(data);
                eur = 1/dol;
            }
            if(!euros.getText().equals("")){
                String data = euros.getText();
                unit = Double.parseDouble(data);
                result = unit*eur;
                
                dolString=round(result);
                eurString=round(unit);
                
                dolares.setBackground(Color.green);
                
            }else if (!dolares.getText().equals("")){
                String data = dolares.getText();
                unit = Double.parseDouble(data);
                result = unit*dol;
                
                dolString=round(unit);
                eurString=round(result);
                
                euros.setBackground(Color.green);
            }else{
                dolString=round(dol);
                eurString=round(eur);
                
                euros.setBackground(Color.green);
                dolares.setBackground(Color.green);
            }
            
            euros.setText(eurString);
            dolares.setText(dolString);
            
            addSteep(new Steep(eurString, dolString));
            
        } catch(NumberFormatException e){
            euros.setText("");
            dolares.setText("");
            clearCells();
            JOptionPane.showMessageDialog(this,"Introducir solo números","Alert",JOptionPane.ERROR_MESSAGE);     
        }  
    }//GEN-LAST:event_jButton1ActionPerformed


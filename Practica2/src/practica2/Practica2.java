    private String round(double number){
        BigDecimal bd = new BigDecimal(number);
        bd = bd.setScale(2, RoundingMode.HALF_UP);
        
        return bd.toString();
    }
    
    private void addSteep(Steep steep){
            
        steeps[iter] = steep;
        
        iter++;
        iterShow=iter-1;
    }

    private void clearCells(){
        dolares.setBackground(Color.white);
        euros.setBackground(Color.white);
    }
    
    private void showPast(){
        
        if(iterShow==iter){
            clearCells();
        }else if(iterShow == 0){
            iterShow = 5;
            euros.setText(steeps[iterShow].getEuro());
            dolares.setText(steeps[iterShow].getDolar());
        }else {
            iterShow--;
            euros.setText(steeps[iterShow].getEuro());
            dolares.setText(steeps[iterShow].getDolar());
        }
    }
     
